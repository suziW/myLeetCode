# 你有1000瓶药水，其中一瓶含有毒药。毒药的效果是延迟的，即给小白鼠喝下含毒的药水后，小白鼠会在一周内死亡。
# 你的任务是在有限的时间内（比如一周），使用尽可能少的小白鼠来准确找出哪一瓶药水含毒。

# 标记药水：将1000瓶药水编号为0到999，然后将每个编号转换为二进制形式。因为1000瓶药水需要10位二进制数，所以每瓶药水都可以用一个10位的二进制数来表示。
# 设计试验：你需要10只小白鼠，每只对应二进制位中的一位。对于每位为1的二进制位，你将对应的药水给对应编号的小白鼠喝。
#           比如，如果某瓶药水的编号是二进制的0011101010，则你会让编号为2、3、4、6、9的小白鼠喝这瓶药水。
# 观察结果：一周后，根据小白鼠的生死情况，你可以读出含毒药水的二进制编码。如果某只小白鼠死了，对应的二进制位是1；如果活着，对应的二进制位是0。
#           这样，10只小白鼠就可以给你提供一个10位的二进制数，这个二进制数对应的十进制编号就是含毒的药水编号。